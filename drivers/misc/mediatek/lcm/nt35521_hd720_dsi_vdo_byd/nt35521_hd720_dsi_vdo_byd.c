static void lcm_get_params(LCM_PARAMS *params)
{
	memset(params, 0, sizeof(LCM_PARAMS));
	params->type   = LCM_TYPE_DSI;

	params->width  = FRAME_WIDTH;
	params->height = FRAME_HEIGHT;

#if (LCM_DSI_CMD_MODE)
    	params->dsi.mode   = CMD_MODE;
#else
    	params->dsi.mode   = SYNC_PULSE_VDO_MODE;
#endif

	// DSI
	/* Command mode setting */
#ifdef USE_LCM_THREE_LANE
	params->dsi.LANE_NUM				= LCM_THREE_LANE;
#else
	params->dsi.LANE_NUM				= LCM_FOUR_LANE; 
#endif
	params->dsi.data_format.format      		= LCM_DSI_FORMAT_RGB888;

	//video mode timing
    	params->dsi.PS=LCM_PACKED_PS_24BIT_RGB888;

    params->dsi.vertical_sync_active				= 2;
    params->dsi.vertical_backporch				= 20;
    params->dsi.vertical_frontporch				= 20;
    params->dsi.vertical_active_line				= FRAME_HEIGHT;

    params->dsi.horizontal_sync_active			= 8;
    params->dsi.horizontal_backporch				= 200;
    params->dsi.horizontal_frontporch				= 200;
    params->dsi.horizontal_active_pixel			= FRAME_WIDTH;

		params->dsi.clk_lp_per_line_enable = 1;                
		params->dsi.esd_check_enable = 1;                      
		params->dsi.customization_esd_check_enable = 0;        
		params->dsi.lcm_esd_check_table[0].cmd          = 0x53;
		params->dsi.lcm_esd_check_table[0].count        = 1;   
		params->dsi.lcm_esd_check_table[0].para_list[0] = 0x24;
		
		    //improve clk quality             
#ifdef USE_LCM_THREE_LANE             
    params->dsi.PLL_CLOCK = 250;//156;

#else
		params->dsi.PLL_CLOCK = 257;//156;
#endif
//    params->dsi.compatibility_for_nvk = 1;
    params->dsi.ssc_disable = 1;
}


static void lcm_init(void)
{
	 //enable VSP & VSN
		SET_RESET_PIN(1);
    SET_RESET_PIN(0);
    MDELAY(10);//Must > 10ms
    SET_RESET_PIN(1);
    MDELAY(120);//Must > 120ms
//    lcm_id_pin_handle();
 
    push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 1);  
    LCD_DEBUG("uboot:boe_nt35521_lcm_init\n");
}

static void lcm_suspend(void)
{
    SET_RESET_PIN(1);
    SET_RESET_PIN(0);
    MDELAY(20);//Must > 10ms
    SET_RESET_PIN(1);
    MDELAY(150);//Must > 120ms
    LCD_DEBUG("kernel:boe_nt35521_lcm_suspend\n");
}

static void lcm_resume(void)
{
    lcm_init();

    push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 1);
    LCD_DEBUG("kernel:boe_nt35521_lcm_resume\n");
}

